<h2 class="text-red custom-row text-center anton">The Native American South Carolina Archive (NASCA)</h2>

<div style="font-size: large;">
<div class="text-dark-grey source-serif">
  <p>NASCA contains the following components:</p>
  <p><b>TRIBES:</b> Information on present day South Carolina Native American entities.</p>
  <p><b>TIMELINE:</b> An interactive timeline with photographs and links to South Carolina Native Americans, their history and material culture, from the last Ice Age 19,000 years ago to the present.</p>
  <p><b>MAP:</b> A map of Native American points of interest around South Carolina, some of which are open to public visitation and education. The map includes a listing of institutions around the state curating various materials, information and/or resources on South Carolina’s Native Americans.</p>
  <p><b>IMAGES:</b> Black and white and color still photographs of various South Carolina tribal entities from the early 1980s to present day.  At this point, most of these photographs were taken by noted University of South Carolina photographer Gene Crediford.</p>
  <p><b>LETTERS:</b> Letters pertaining to the Catawba Indians in South Carolina Governor’s correspondence files from 1849 to 1887.  The originals are held at the South Carolina Department of Archives and History.</p>
  <p><b>VIDEOS:</b> Five short films on South Carolina Native American Prehistory (Ice Age to European Contact) as known from archaeological research, and video tour of the Native American Studies Center at the University of South Carolina Lancaster.</p>
  <p>NASCA is an evolving project and new materials will be added periodically.</p>
</div>

<hr class="red"/>

<div class="text-dark-grey source-serif">
  <h3 class="anton">PROJECT OVERVIEW:</h3>
  <p>A joint project of the Native American Studies Archive at the University of South Carolina Lancaster, the University of South Carolina’s Institute for Southern Studies, and the University of South Carolina Libraries’ Digital Collections.</p>
  <p>NASCA will expand the research and service impact of the University of South Carolina Lancaster’s Native American Studies Center and Archive, increasing the use and footprint of its resources and creating new possibilities for collaboration.</p>
  <p>Native South Carolinians have been, and continue to be, an essential part of our state’s history. However, the average South Carolinian would be hard-pressed to name more than one of the state-recognized tribes, and might think of the history of Native South Carolina largely ending with the colonial-era Yamasee War.  NASCA looks to correct these misconceptions, helping to tell the story of Native South Carolina with complexity, nuance, and respect. In pursuing the goals, this project will take the form of an evolving website, providing access to significant primary source materials and other resources.</p>
</div>

